class Calculator {

	void sum(int arg1,int arg2) {	
		System.out.printf("SUM : %s%n",(arg1+arg2));
	}

	void sum(int arg1,int arg2,int arg3) {	
		System.out.printf("SUM : %s%n",(arg1+arg2+arg3));
	}

	void sum(int arg1,double arg2) {	
		System.out.printf("SUM : %s%n",(arg1+arg2));
	}

	void sum(double arg1,int arg2) {	
		System.out.printf("SUM : %s%n",(arg1+arg2));
	}
}